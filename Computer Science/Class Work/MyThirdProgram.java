public class MyThirdProgram {
   public static void main(String[] args) {
      
      System.out.println();
      f();
      l();
      
      System.out.println();
      f();
      m();
      l();

      System.out.println();
      l();
      comma();
      f();
      
      System.out.println();
      l();
      comma();
      f();
      m();
      
      System.out.println();
      
      }
      public static void f() {
         System.out.print("Tyler ");
      }
      public static void m() {
         System.out.print("Matthew ");
      }
      public static void l() {
         System.out.print("Palmgren");
      }
      public static void comma() {
         System.out.print(", ");
         }
}